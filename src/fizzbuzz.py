# -----------------------------------------------------------------------------
# Variables
# -----------------------------------------------------------------------------
fizz_text = 'fizz'
buzz_text = 'buzz'

fizz_num = 3
buzz_num = 5

start_range = 1
end_range = 100

# -----------------------------------------------------------------------------
# Functions
# -----------------------------------------------------------------------------
def main():
    for i in range(start_range, end_range + 1):
        output = ''

        if i % fizz_num == 0:
            output = output + fizz_text
        
        if i % buzz_num == 0:
            output = output + buzz_text
        
        if output == '':
            output = str(i)
    
        print(output)




if __name__ == '__main__':
    main()
